package tutorial11;

public class Main11 {

	public static void main(String[] args) {
		
		int a = 10;
		System.out.println("현재의a는" + a + "입니다." );
		a++;
		System.out.println("현재의a는" + a + "입니다." );
		System.out.println("현재의a는" + ++a + "입니다." );
		System.out.println("현재의a는" + a++ + "입니다." );
		System.out.println("현재의a는" + a + "입니다." );
		// 증감연산자 활용

	}
}
